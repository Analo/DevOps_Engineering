# Give example of fsum and sum function of math library.

# Python code to demonstrate use
# of math.fsum() function

# fsum() is found in math library

import math

# range(10)
print(math.fsum(range(10)))

# Integer list
arr = [1, 4, 6]
print(math.fsum(arr))

# Floating point list
arr = [2.5, 2.4, 3.09]
print(math.fsum(arr))

# fsum() is inbuilt function in Python, used to find sum between some range or an iterable.
# To use this function we need to import the math library.